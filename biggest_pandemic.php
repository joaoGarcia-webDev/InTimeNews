        <h5 class="text-title-curiosity">O COVID-19 não foi a única doença que causou uma pandemia. Veja a seguir as 5 maiores pandemias mundiais:</h5>
        <br><br>
        <ol class="text-curiosity-covid">
            <li><b>Peste bubônica</b></li>
            A peste bubônica é causada pela bactéria Yersinia pestis e pode se disseminar pelo contato com pulgas e roedores infectados. Seus sintomas incluem inchaço dos gânglios linfáticos na virilha, na axila ou no pescoço. Outros sinais são febre, calafrios, dor de cabeça, fadiga e dores musculares.

            A doença é considerada, historicamente, a causadora da Peste Negra, que assolou a Europa no século 14, matando entre 75 milhões e 200 milhões pessoas na antiga Eurásia. No total, a praga pode ter reduzido a população mundial de 450 milhões de pessoas para 350 milhões.
            
            <li><b>Varíola</b></li>
            A doença atormentou a humanidade por mais de 3 mil anos. O faraó egípcio Ramsés II, a rainha Maria II da Inglaterra e o rei Luís XV da França tiveram a temida “bixiga”. O vírus Orthopoxvírus variolae era transmitido de pessoa para pessoa, por meio das vias respiratórias. Os sintomas eram febre, seguida de erupções na garganta, na boca e no rosto. Felizmente, a varíola foi erradicada do planeta em 1980, após campanha de vacinação em massa.

            <li><b>Cólera</b></li>
            Sua primeira epidemia global, em 1817, matou centenas de milhares de pessoas. Desde então, a bactéria Vibrio cholerae sofre diversas mutações e causa novos ciclos epidêmicos de tempos em tempos e, portanto, ainda é considerada uma pandemia.
            Sua transmissão acontece a partir do consumo de água ou alimentos contaminados, e é mais comum em países subdesenvolvidos. Um dos mais atingidos pela cólera foi o Haiti, em 2010. O Brasil já teve vários surtos da doença, principalmente em áreas mais pobres do Nordeste. No Iêmen, em 2019, mais de 40 mil pessoas morreram devido à enfermidade..

            Os sintomas são diarreia intensa, cólicas e enjoo. Apesar de existir vacina contra a doença, ela não é 100% eficaz. O tratamento é à base de antibióticos.
            <li><b>Gripe Espanhola</b></li>
            Acredita-se que entre 40 milhões e 50 milhões de pessoas tenham morrido na pandemia de Gripe Espanhola de 1918, causada por um subtipo de vírus influenza. Mais de um quarto da população mundial na época foi infectada e até o então presidente do Brasil, Rodrigues Alves, morreu da doença, em 1919. O vírus veio da Europa, a bordo do navio Demerara. O transatlântico desembarcou passageiros infectados em Recife, Salvador e Rio de Janeiro.

            Os sintomas da doença eram muito parecidos com os do atual coronavírus Sars-CoV-2, e não existia cura. Em São Paulo, a população foi atrás de um remédio caseiro feito com cachaça, limão e mel. De acordo com o Instituto Brasileiro da Cachaça, foi dessa receita supostamente terapêutica que nasceu a caipirinha.
            <li><b>Gripe Suína (H1N1)</b></li>
            O vírus H1N1, causador da chamada gripe suína, foi o primeiro a gerar uma pandemia no século 21. O vírus surgido em porcos no México, em 2009, e se espalhou rapidamente pelo mundo, matando 16 mil pessoas. No Brasil, o primeiro caso foi confirmado em maio daquele ano e, no fim de junho, 627 pessoas estavam infectadas no país, de acordo com o Ministério da Saúde.

            O contágio acontece a partir de gotículas respiratórias no ar ou em uma superfície contaminada. Seus sintomas são os mesmos de uma gripe comum: febre, tosse, dor de garganta, calafrio e dor no corpo.
