        conhecimento da doença. Além disso, ambas reforçaram a desigualdade social existente: os primeiros
        disseminadores eram viajantes vindos do Exterior, geralmente das camadas de renda média e alta; mas a
        contaminação atingiu rápida e amplamente os grupos de baixa renda, dadas suas condições socioambientais.
        </p>

        <p align="justify">Prosseguindo com as semelhanças, em ambas, houve teorias conspiratórias: na gripe espanhola, os dois lados na
        Primeira Guerra Mundial acusavam-se reciprocamente; na covid-19, a disputa Estados Unidos-China afetou as
        responsabilizações. Também nos dois casos, medicamentos “fantásticos”, jamais confirmados pela ciência, tiveram
        seus dias de glória: grippina e quinino, na Espanhola; cloroquina, hidroxicloroquina e ivermectina na covid-19.
        A curiosidade é que a cloroquina, há muito tempo, passou a substituir o quinino no combate à malária, com
        eficácia e validação da ciência. Outra semelhança significativa é a repulsa ao uso de máscaras por segmentos
        minoritários, porém barulhentos.
        </p>
        <p align="justify">
        Um aspecto notável é o do avanço simultâneo do conhecimento e da ignorância, no século entre as duas pandemias.
        O que explica que, não obstante o grande avanço e popularização do conhecimento científico, argumentos
        anticientíficos continuem a prosperar um século após? A hipótese básica é que o negacionismo científico, antes
        restrito a grupos articulados em torno de interesses religiosos ou econômicos específicos, e aos amantes de
        teorias da conspiração, tem ganhado corações e mentes nos últimos anos por intermédio das redes sociais.
        </p>
        <p align="justify">
        Com a chegada da covid-19, o fenômeno se intensificou. E, assim, o que era a contracorrente tornou-se, em alguns
        casos, discurso oficial e política de Estado. Esse processo de institucionalização do negacionismo na figura de
        líderes políticos teria comprometido a eficácia das medidas de combate à pandemia em países como Brasil, Estados
        Unidos e Reino Unido.
        </p>
        <p align="justify">

        A saúde pública é outro tema em comum. O Brasil da Velha República praticamente não possuía nada digno desse
        nome. Mas, um século depois, o SUS sofreu sucessivos cortes e desativações no quinquênio 2015-2020. Com isso, 40
        mil leitos foram desativados no início da covid-19. Houve fortes restrições orçamentárias para a compra de
        equipamentos e contratação de novos profissionais, resultando no trabalho hercúleo (e heróico) dos profissionais
        da saúde.
        </p>
        <p align="justify">

        Durante a gripe espanhola, destacou-se o sanitarista Carlos Chagas no comando das ações de combate à pandemia.
        Mas bem diferente foi o percurso da saúde pública no Brasil da covid-19. Dois médicos sucederam-se no Ministério
        da Saúde, buscando cumprir protocolos científicos, mas o confronto não foi com a imprensa ou a opinião pública,
        como em 1918-1919. Agora, o bloqueio veio do chefe do Executivo e suas diretrizes, em confronto direto com as
        entidades médicas nacionais e internacionais e com a comunidade científica.
        </p>
        <p align="justify">

        O terceiro ministro da Saúde, um general, preencheu diversos cargos de responsabilidade do órgão com membros de
        sua corporação. Assim, os sucessivos desencontros com a comunidade médica e científica culminaram com a tragédia
        sanitária e humana em Manaus. O quarto ministro, empossado no final de março de 2021, adota discurso mais
        consentâneo com a comunidade médica, mas entra cercado de ceticismo pela sociedade.
        </p>
        <p align="justify">

        “Em 1918, não tínhamos nenhuma autoridade política ou científica apoiando o uso de sal de quinino, como
        Bolsonaro faz hoje com a cloroquina”, compara a historiadora e antropóloga Lilia Schwarcz, professora da USP e
        da Universidade Princeton, nos Estados Unidos.
        </p>
        <p align="justify">

        “Ao contrário do que aconteceu no início do século 20, vemos hoje uma vontade deliberada do governo de sabotar
        todas as medidas de prevenção e contra a disseminação do coronavírus”, analisa o professor João Malaia, da
        Universidade Federal de Santa Maria. Segundo especialistas ouvidos pela BBC News Brasil, o principal ponto que
        separa as crises da gripe espanhola e da covid-19 está na atuação dos órgãos federais.
        </p>
        <p align="justify">

        Os governantes da época da gripe espanhola, como alguns da covid-19, também começaram negando a crise e
        criticando o suposto alarmismo da imprensa. Mas logo convenceram-se da necessidade de adotar providências mais
        drásticas. “As autoridades sanitárias recomendaram que as pessoas se mantivessem em casa e não fossem aos locais
        públicos. Decretos proibiram algumas práticas bastante comuns no período, como cuspir no meio da rua”, conta
        historiadora Daiane Silveira Rossi, pós-doutoranda pela Fiocruz e Faperj. A situação exigiu a construção rápida
        de hospitais de campanha e locais para isolamento de infectados.
        </p>
        <p align="justify">

        Na atualidade, uma Comissão Parlamentar de Inquérito apura as ações e omissões do Poder Executivo. Pesquisa da
        Universidade de Michigan e da Fundação Getulio Vargas mostra que o Brasil tinha os mecanismos necessários para
        combater a covid-19, mas o presidente optou pela ‘pseudociência’ e pelo ‘negacionismo’.
        </p>
        <p align="justify">

        Na espanhola, durante a República Velha, houve desencontros com o governo federal. Mas, enquanto a pandemia
        arrefecia na capital federal, ocorrências dramáticas em alguns estados eram tratadas como “problema dos
        presidentes” (governadores) pelo chefe do governo federal. Já agora, o presidente empenhou-se em guerra com os
        governadores, buscando travar suas ações e responsabilizá-los pelos insucessos.
        </p>
        <p align="justify">

        Segundo estudo da Universidade de Michigan e FGV, o resultado só não foi mais trágico porque o país tem
        infraestrutura de vigilância sanitária desenvolvida para lidar com pandemias. Desse modo, rede de atenção
        primária do Sistema Único de Saúde foi essencial para mitigar o impacto da Covid19. A atuação dos estados e
        prefeituras também ajudou a controlar o caos no País.
        </p>
        <p align="justify">

        Importante registrar a campanha ativa contra a vacinação por parte do presidente da República ao longo do ano de
        2020 e no primeiro bimestre de 2020. O jornal francês Le Figaro notou que a campanha do presidente contra a
        imunização, afirmando que não vai tomar a vacina e criticando a iniciativa do STF de torná-la obrigatória, foi
        “uma atitude singular e única nas democracias”. Le Figaro destaca que o Brasil tem uma experiência reconhecida
        na produção de vacinas e em campanhas de imunização da população, “graças a instituições de pesquisa renomadas
        como o Instituto Butantan e a Fiocruz”.
