        separados por um século. As duas diferenças mais expressivas são a produção de desinformação e descrédito na
        ciência e na informação das mídias convencionais, no caso da Covid, em escala muito superior à do passado,
        envolvendo o núcleo governante e círculos ao seu redor, com grande capilaridade por meio das redes sociais; e na
        atitude do governante máximo e sua equipe – com descarte dos destoantes –, de contrapor-se aos protocolos e
        recomendações do meio científico e das entidades médicas.
